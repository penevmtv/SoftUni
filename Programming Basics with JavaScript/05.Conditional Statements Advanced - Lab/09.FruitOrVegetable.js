function solve(input) {
    if (input === "banana" || input === "apple" || input === "kiwi" || input === "cherry" || input === "lemon" || input === "grapes")
      console.log("fruit");
    else if (input === "tomato" || input === "cucumber" || input === "pepper" || input === "carrot")
      console.log("vegetable");
    else
      console.log("unknown");
  }